    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Derivador derivar = new Derivador();
        DecimalFormat decimales = new DecimalFormat("0.00000000"); 
        NewtonRaphson obj = new NewtonRaphson();
        double x = Double.parseDouble(txtx.getText()), y = Double.parseDouble(txty.getText()), J[][], F[][], Delta[][], error = 1;
        String dx = derivar.Derivar(txtf1.getText()), dy = derivar.DerivarY(txtf1.getText()),
                dx2 = derivar.Derivar(txtf2.getText()), dy2 = derivar.DerivarY(txtf2.getText()),
                resultadoj = "";
        lbldx.setText(dx);
        lbldy.setText(dy);
        lbldx2.setText(dx2);
        lbldy2.setText(dy2);
        txax.setText("");
        txaj.setText("");
        txaf.setText("");
        txad.setText("");
        txaerror.setText("");
        //posiblemente use un ciclo
        while (error >= .001) {
            txax.append("x " + decimales.format(x) + "\n");
            txax.append("y " + decimales.format(y) + "\n");
            resultadoj = "";
            J = obj.j(txtf1.getText(), txtf2.getText(), dx, dy, dx2, dy2, x, y);
            for (int i = 0; i < 2; i++) {
                for (int n = 0; n < 2; n++) {
                    resultadoj += decimales.format(J[i][n]) + " ";
                }
                resultadoj += "\n";
            }
            txaj.append(resultadoj );
            F = obj.f(txtf1.getText(), txtf2.getText(), x, y);
            txaf.append(decimales.format(F[0][0]) + "\n");
            txaf.append(decimales.format(F[1][0]) + "\n");
            Delta = obj.delta();
            txad.append(decimales.format(Delta[0][0]) + "\n");
            txad.append(decimales.format(Delta[1][0]) + "\n");
            error = Math.sqrt(Math.pow(Delta[0][0], 2) + Math.pow(Delta[1][0], 2));
            txaerror.append("" + decimales.format(error) + "\n");
            txax.append("\n");
            txaf.append("\n");
            txaj.append("\n");
            txad.append("\n");
            txaerror.append("\n\n");
            x = obj.proximoValor(x, Delta[0][0]);
            y = obj.proximoValor(y, Delta[1][0]);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

