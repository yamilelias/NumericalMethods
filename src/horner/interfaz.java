    private void escalarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_escalarKeyTyped
        char funcion = evt.getKeyChar();
        boolean terminar = true;
        for (int i = 0; i < permitidos.length; i++) {
            if (funcion == permitidos[i]) {
                terminar = false;
            }
        }
        if (terminar) {
            evt.consume();
        }
    }//GEN-LAST:event_escalarKeyTyped

